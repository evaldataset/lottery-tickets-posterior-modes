#!/usr/bin/env python
from __future__ import annotations

import argparse
import csv
import json
import math
from pathlib import Path


FIELDS = [
    "label",
    "num_runs",
    "dense_acc",
    "imp_acc",
    "sparsity",
    "posterior",
    "random",
    "chain_start",
    "posterior_to_chain_start",
    "dense_magnitude",
    "rewind_magnitude",
    "snip",
    "synflow",
    "state_clusters",
    "function_clusters",
    "gate1_passed",
]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--entry",
        action="append",
        required=True,
        help="Label:path/to/summary.json[:path/to/eval.json]",
    )
    parser.add_argument("--out-csv", type=Path, default=Path("runs/results_snapshot.csv"))
    parser.add_argument("--out-md", type=Path, default=Path("docs/results_snapshot.md"))
    return parser.parse_args()


def summary_mean(payload: dict, field: str) -> float:
    return float(payload["summary"][field]["mean"])


def optional_summary_mean(payload: dict, field: str) -> float:
    if field not in payload["summary"]:
        return math.nan
    return float(payload["summary"][field]["mean"])


def load_entry(text: str) -> dict[str, float | str | bool]:
    parts = text.split(":")
    if len(parts) not in {2, 3}:
        raise ValueError("--entry must be Label:summary.json[:eval.json]")
    label = parts[0]
    summary_path = Path(parts[1])
    eval_path = Path(parts[2]) if len(parts) == 3 else None
    with summary_path.open(encoding="utf-8") as f:
        summary = json.load(f)
    gate1_passed = ""
    if eval_path is not None and eval_path.exists():
        with eval_path.open(encoding="utf-8") as f:
            gate1_passed = json.load(f)["gate1_passed"]
    return {
        "label": label,
        "num_runs": summary["num_runs"],
        "dense_acc": summary_mean(summary, "dense_accuracy"),
        "imp_acc": summary_mean(summary, "imp_accuracy"),
        "sparsity": summary_mean(summary, "imp_sparsity"),
        "posterior": summary_mean(summary, "posterior_jaccard_mean"),
        "random": summary_mean(summary, "random_jaccard_mean"),
        "chain_start": summary_mean(summary, "chain_start_magnitude_to_imp_jaccard_mean"),
        "posterior_to_chain_start": summary_mean(
            summary, "posterior_to_chain_start_magnitude_jaccard_mean"
        ),
        "dense_magnitude": summary_mean(summary, "dense_magnitude_to_imp_jaccard"),
        "rewind_magnitude": optional_summary_mean(
            summary, "rewind_magnitude_to_imp_jaccard"
        ),
        "snip": optional_summary_mean(summary, "snip_to_imp_jaccard"),
        "synflow": optional_summary_mean(summary, "synflow_to_imp_jaccard"),
        "state_clusters": summary_mean(summary, "state_num_clusters"),
        "function_clusters": summary_mean(summary, "function_num_clusters"),
        "gate1_passed": gate1_passed,
    }


def fmt(value: float | str | bool) -> str:
    if isinstance(value, bool):
        return "pass" if value else "fail"
    if isinstance(value, float):
        if math.isnan(value):
            return "n/a"
        return f"{value:.4f}"
    return str(value)


def write_markdown(rows: list[dict[str, float | str | bool]], path: Path) -> None:
    headers = [
        "Dataset",
        "Runs",
        "Dense Acc",
        "IMP Acc",
        "Sparsity",
        "Posterior",
        "Random",
        "Chain Start",
        "Post-Chain",
        "Dense Mag",
        "Rewind Mag",
        "SNIP",
        "SynFlow",
        "Gate1",
    ]
    keys = [
        "label",
        "num_runs",
        "dense_acc",
        "imp_acc",
        "sparsity",
        "posterior",
        "random",
        "chain_start",
        "posterior_to_chain_start",
        "dense_magnitude",
        "rewind_magnitude",
        "snip",
        "synflow",
        "gate1_passed",
    ]
    lines = [
        "# Results Snapshot",
        "",
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join(["---"] + ["---:"] * (len(headers) - 1)) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(fmt(row[key]) for key in keys) + " |")
    lines.extend(
        [
            "",
            "Interpretation:",
            "",
            "Gate1 fails when posterior masks beat random but do not beat",
            "chain-start magnitude, remain close to chain-start supports, or are",
            "dominated by dense or rewind magnitude controls.",
            "",
            "This file is generated by `scripts/build_results_table.py`.",
        ]
    )
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    args = parse_args()
    rows = [load_entry(entry) for entry in args.entry]
    args.out_csv.parent.mkdir(parents=True, exist_ok=True)
    with args.out_csv.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=FIELDS)
        writer.writeheader()
        writer.writerows(rows)
    write_markdown(rows, args.out_md)
    print(f"wrote {args.out_csv}")
    print(f"wrote {args.out_md}")


if __name__ == "__main__":
    main()
